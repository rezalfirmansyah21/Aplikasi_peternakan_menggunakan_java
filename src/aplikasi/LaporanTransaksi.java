             String tanggal ="YYYY/MM/dd";
        SimpleDateFormat format = new SimpleDateFormat(tanggal);
        String Mulai = String.valueOf(format.format(calendarmulai.getDate()));
        String Selesai = String.valueOf(format.format(calendarselesai.getDate()));
        if("".equals(Mulai) || "".equals(Selesai)){
            JOptionPane.showMessageDialog(null, "Lengkapi data");
        }else{
            String filename = path+"transaksi.jrxml";
            JasperReport jasrep;
            JasperPrint japri;
            JasperViewer javie = null;
            HashMap param = new HashMap(3);
            JasperDesign jasdes;
            try {
                param.put("tglmulai",Mulai);
                param.put("tglselesai",Selesai);
                param.put("user",txtuser.getText());
                File report = new File(filename);
                jasdes = JRXmlLoader.load(report);
                jasrep = JasperCompileManager.compileReport(jasdes);
                japri = JasperFillManager.fillReport(jasrep,param,koneksi.getKoneksi());
                javie.viewReport(japri,false);
            } catch (Exception e) {
                System.out.print("gagal ngprint");
            }
        }
